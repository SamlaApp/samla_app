class User {
  final int id;
  final String name;
  final String email;
  final String username;
  final String phone;
  final String dateOfBirth;
  final String accessToken;

  User({
    required this.id,
    required this.name,
    required this.email,
    required this.username,
    required this.phone,
    required this.dateOfBirth,
    required this.accessToken,
  });
}

